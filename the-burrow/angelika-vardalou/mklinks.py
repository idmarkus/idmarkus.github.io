import numpy as np
from skimage import io
import os

originals = originals.split("\n")
thumbs_large = thumbs_large.split("\n")
thumbs_medium = thumbs_medium.split("\n")

os.makedirs("img", exist_ok=True)
lines = ""
for i, url in enumerate(originals):
    thumb_url = thumbs_large[i]
    # print("Getting {}, {}".format(i, url))
    img = np.asarray(io.imread(thumb_url))
    if img.size < 2:
        print("Error for {}".format(url))
        continue
    original_name = url.split("/")[-1][:-4]
    html_single = '<html><head><title>{}</title></head><body><div style="text-align:center;"><img src="{}"></div></body></html>'
    html_single = html_single.format(original_name, url);
    with open("img/{}.html".format(original_name), "w+") as f:
        f.write(html_single)

    local_url = "img/{}.html".format(original_name)
    lines += '<a href="{}"><img width="{}" height="{}" src="{}"></a>\n'.format(local_url, img.shape[1], img.shape[0], thumb_url)

print(lines)
